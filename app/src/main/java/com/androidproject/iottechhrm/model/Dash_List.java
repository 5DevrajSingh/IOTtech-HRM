package com.androidproject.iottechhrm.model;

public class Dash_List {
    public int image;
    public String name;

    public Dash_List(int image, String name) {
        this.image = image;
        this.name = name;
    }

    public int getImage() {
        return image;
    }

    public String getName() {
        return name;
    }
}
